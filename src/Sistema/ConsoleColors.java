package Sistema;

/*
 * This class can change the font colors of the catch return statments to red.
 * In order to help the user as a guide to find the mistake.
 * -Works as a Jacob Nielsen's Usability heuristics.
 * @author Hecoal
 */
public class ConsoleColors {
    
    public static final String RESET = "\033[0m";
    public static final String RED = "\u001B[41m";
    public static final String WHITE = "\u001B[47m";
    public static final String GREEN = "\u001B[42m";
   
}
